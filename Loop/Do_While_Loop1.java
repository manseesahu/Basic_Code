package Loop;

public class Do_While_Loop1 {

	public static void main(String[] args) {
		// do{
		// do something
	    //}while(condition);
		//In FOR LOOP we can check the condition "first"
		//In WHILE LOOP "firstly" we can check the condition 
		//In DO WHILE first we have to do work then check the condition
		int i =0;
		do {
			System.out.println(i);
			i=i+1;
		} while (i<11);
		}

	}


