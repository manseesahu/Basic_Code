package test1;
import java.util.*;

public class Addition_by_Scanner {
	//Take 2 variables a and b and print their sum.
	public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int a = sc.nextInt();
    int b = sc.nextInt();
    int sum = a+b;
    System.out.println(sum);
	}

}
